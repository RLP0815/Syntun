package com.syntun.entity;

/**
 * 
 */
public class WebError {
    //主键ID
	private int id;
	//
    private String urlGroup;
	//
    private String errContentStr;
    //
    private int errContentResult;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUrlGroup() {
		return urlGroup;
	}
	public void setUrlGroup(String urlGroup) {
		this.urlGroup = urlGroup;
	}
	public String getErrContentStr() {
		return errContentStr;
	}
	public void setErrContentStr(String errContentStr) {
		this.errContentStr = errContentStr;
	}
	public int getErrContentResult() {
		return errContentResult;
	}
	public void setErrContentResult(int errContentResult) {
		this.errContentResult = errContentResult;
	}
    
}