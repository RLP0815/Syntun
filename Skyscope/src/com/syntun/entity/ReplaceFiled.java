public class ReplaceFiled {
    //主键ID
	private int id;
    //原字段
	private String oldFiled;
    //统一字段
	private String unityFiled;
    
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getOldFiled() {
		return oldFiled;
	}
	public void setOldFiled(String oldFiled) {
		this.oldFiled = oldFiled;
	}
	public String getUnityFiled() {
		return unityFiled;
	}
	public void setUnityFiled(String unityFiled) {
		this.unityFiled = unityFiled;
	}
    
    
    
}