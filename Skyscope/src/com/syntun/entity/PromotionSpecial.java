package com.syntun.entity;

/**
 * 
 */
public class PromotionSpecial {
    //主键ID
	private int id;
	//平台ID
	private int platformId;
	//平台名称
	private String platformName;
    //促销详情
	private String promotionInfo;
    //第一种促销
	private String promotionFirst;
	//第二种促销
	private String promotionSecond;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getPlatformId() {
		return platformId;
	}
	public void setPlatformId(int platformId) {
		this.platformId = platformId;
	}
	public String getPlatformName() {
		return platformName;
	}
	public void setPlatformName(String platformName) {
		this.platformName = platformName;
	}
	public String getPromotionInfo() {
		return promotionInfo;
	}
	public void setPromotionInfo(String promotionInfo) {
		this.promotionInfo = promotionInfo;
	}
	public String getPromotionFirst() {
		return promotionFirst;
	}
	public void setPromotionFirst(String promotionFirst) {
		this.promotionFirst = promotionFirst;
	}
	public String getPromotionSecond() {
		return promotionSecond;
	}
	public void setPromotionSecond(String promotionSecond) {
		this.promotionSecond = promotionSecond;
	}

	
    
}