package com.syntun.entity;

/**
 * 
 */
public class PromotionList {
    //主键ID
	private int id;
    //促销ID
	private int promotionId;
    //促销标准名称
	private String promotionColName;
    
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getPromotionId() {
		return promotionId;
	}
	public void setPromotionId(int promotionId) {
		this.promotionId = promotionId;
	}
	public String getPromotionColName() {
		return promotionColName;
	}
	public void setPromotionColName(String promotionColName) {
		this.promotionColName = promotionColName;
	}
    
}