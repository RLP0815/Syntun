package com.syntun.entity;

/**
 * 关于价格的临时对象，用于整合价格，销量，评论时使用
 * 
 * @author tuo
 * 
 */
public class PricePromotionInfo {
	
	public static void main(String[] args) {
		
	}
	
	/**
	 * 促销id集合
	 */
	private String promotionIds;

	/**
	 * 零售价
	 */
	private String lingshoujia = null;

	/**
	 * 促销价
	 */
	private String promotionPrice = null;

	/**
	 * 平均成交价
	 */
	private String avgPrice;

	/**
	 * 价格比率
	 */
	private Double priceOff;
	/**
	 * 最大价格
	 */
	private Double maxPrice;

	/**
	 * 最小价格
	 */
	private Double minPrice;

	/**
	 * 市场均价
	 */
	private String shichangjunjia;

	public Double getPriceOff() {
		return priceOff;
	}

	public void setPriceOff(Double priceOff) {
		this.priceOff = priceOff;
	}

	public String getShichangjunjia() {
		return shichangjunjia;
	}

	public void setShichangjunjia(String shichangjunjia) {
		this.shichangjunjia = shichangjunjia;
	}

	public Double getMaxPrice() {
		return maxPrice;
	}

	public void setMaxPrice(Double maxPrice) {
		this.maxPrice = maxPrice;
	}

	public Double getMinPrice() {
		return minPrice;
	}

	public void setMinPrice(Double minPrice) {
		this.minPrice = minPrice;
	}

	public String getPromotionIds() {
		return promotionIds;
	}

	public void setPromotionIds(String promotionIds) {
		this.promotionIds = promotionIds;
	}

	public String getLingshoujia() {
		return lingshoujia;
	}

	public void setLingshoujia(String lingshoujia) {
		this.lingshoujia = lingshoujia;
	}

	public String getPromotionPrice() {
		return promotionPrice;
	}

	public void setPromotionPrice(String promotionPrice) {
		this.promotionPrice = promotionPrice;
	}

	public String getAvgPrice() {
		return avgPrice;
	}

	public void setAvgPrice(String avgPrice) {
		this.avgPrice = avgPrice;
	}

}
