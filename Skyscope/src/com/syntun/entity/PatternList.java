package com.syntun.entity;

/**
 * 
 */
public class PatternList {
    //主键ID
	private int id;
    //优先级（程序按优先级访问，优先级靠前的访问成功后不进行后续访问）
	private int priority;
    //正则类型1为主正则，负责匹配满减信息，2未每满减中出现的最大金额抓取正则
	private int patternType;
    //正则内容
	private String patternStr;
    //满的group，即满在正则内容中的位置
	private int manGroup;
    //减的group，即减在正则内容中的位置
	private int jieGroup;
    //是否是满多少打折（1是，0不是）
	private int isDazhe;
    //是否每满（1是，0不是）
	private int isMeiman;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getPriority() {
		return priority;
	}
	public void setPriority(int priority) {
		this.priority = priority;
	}
	public int getPatternType() {
		return patternType;
	}
	public void setPatternType(int patternType) {
		this.patternType = patternType;
	}
	public String getPatternStr() {
		return patternStr;
	}
	public void setPatternStr(String patternStr) {
		this.patternStr = patternStr;
	}
	public int getManGroup() {
		return manGroup;
	}
	public void setManGroup(int manGroup) {
		this.manGroup = manGroup;
	}
	public int getJieGroup() {
		return jieGroup;
	}
	public void setJieGroup(int jieGroup) {
		this.jieGroup = jieGroup;
	}
	public int getIsDazhe() {
		return isDazhe;
	}
	public void setIsDazhe(int isDazhe) {
		this.isDazhe = isDazhe;
	}
	public int getIsMeiman() {
		return isMeiman;
	}
	public void setIsMeiman(int isMeiman) {
		this.isMeiman = isMeiman;
	}
    
}